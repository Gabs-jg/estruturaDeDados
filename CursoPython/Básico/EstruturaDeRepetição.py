#Estrutura de repetição For:
#Crescente:
#Contagem de 0 a 9
for n in range(10):
    print(n);

#Contagem de 5 a 15
for n in range(5, 16):
    print(n);

#Decrescente:
#Contagem de 10 a 1:
for n in range(10, 0, -1):
    print(n);

#Estrutura de repetição While:
#Crescente:
#Contagem de 1 a 15:
#Declaração de variável:
x= 1;
while x <= 15:
    print(x);
    x = x + 1;

#Crescente:
#Contagem de 15 a 1:
#Declaração de variável:
x= 15;
while x <= 1:
    print(x);
    x = x - 1;